package com.example.codetest;

public class CodeTest {
    public static void main(String[] args) {

            int num1 = 1;
            int num2 = 1;
            int num3 = 0;

            if (num1 + num2 == num3) {
                System.out.println("+");
            }
            if (num1 - num2 == num3) {
                System.out.println("-");
            }
            if (num1 * num2 == num3) {
                System.out.println("*");
            }
            if (num1 / num2 == num3) {
                System.out.println("/");
            } else {
                System.out.println("None");
            }
        }
    }
